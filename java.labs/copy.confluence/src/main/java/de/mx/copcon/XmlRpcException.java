public class XmlRpcException extends Exception {

    public XmlRpcException() {
    }

    public XmlRpcException(String message) {
        super(message);
    }

    public XmlRpcException(String message, Throwable cause) {
        super(message, cause);
    }

    public XmlRpcException(Throwable cause) {
        super(cause);
    }
    
    
    
}
