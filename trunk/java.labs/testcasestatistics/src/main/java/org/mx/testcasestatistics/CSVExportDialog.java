    private void complete() throws IOException {

        File file = null;

        PriorityQueue queue = new PriorityQueue();
        queue.add(new File("."));
        CSVWriter writer = new CSVWriter(
                    new FileWriter("importet.csv"), ',');

        writer.writeNext(new String[]{"OGE_ID", "Name", "Beschreibung", "Vorraussetzungen", "Type", "Prio", "Duration", "Stepnum", "Stepbeschr", "Stepresult"});

        while (!queue.isEmpty()) {

            file = (File) queue.poll();

            queue.addAll(Arrays.asList(file.listFiles(new FileFilter() {

                public boolean accept(File pathname) {

                    return pathname.isDirectory();

                }
            })));

            

            for (File xlsFile : file.listFiles(new FilenameFilter() {

                public boolean accept(File dir, String name) {
                    return name.toLowerCase().startsWith("testcase") && name.contains(".xls");
                }
            })) {

                System.out.println("process " + xlsFile.getName());
                try {

                    Workbook w = new HSSFWorkbook(new FileInputStream(xlsFile));


                    for (int i = 1; i < w.getNumberOfSheets(); i++) {
                        try {
                            String[] line = new String[10];
                            line[0] = xlsFile.getName().substring(0, xlsFile.getName().indexOf('.'));
                            Sheet sh = w.getSheetAt(i);


                            Row r1 = sh.getRow(0);
                            //System.out.println("TestCaseName: " + r1.getCell(2).getStringCellValue());
                            line[1] = NewMainFrame.getStringValue(r1.getCell(2));
                            Row r3 = sh.getRow(2);
                            line[2] = NewMainFrame.getStringValue(r3.getCell(2)); //Beschreibung
                            Row r8 = sh.getRow(7);
                            line[3] = NewMainFrame.getStringValue(r8.getCell(2)); //Vorraussetungen

                            Row r5 = sh.getRow(4);
                            //System.out.println("Regr: " + r5.getCell(6).getStringCellValue());

                            line[4] = (!NewMainFrame.getStringValue(r5.getCell(6)).isEmpty()) ? "Regression" : "Functional";

                            //System.out.println("Prio: " + r4.getCell(6).getNumericCellValue());
                            Row r4 = sh.getRow(3);
                            switch (new Double(NewMainFrame.getNumericValue(r4.getCell(6))).intValue()) {
                                case 1:
                                    line[5] = "Critical";
                                    break;
                                case 2:
                                    line[5] = "Major";
                                    break;
                                case 3:
                                    line[5] = "Minor";
                                    break;
                                default:
                                    line[5] = "Blocker";
                                    break;
                            }

                            Row r11 = sh.getRow(10);
                            line[6] = NewMainFrame.getStringValue(r11.getCell(6)).replaceAll("min", ""); //duration
                            try {
                                Double.valueOf(line[6]);
                            } catch (NumberFormatException e) {
                                line[6] = "";
                            }
                            
                            Row r = sh.getRow(16);

                            try {
                                while (!NewMainFrame.getStringValue(r.getCell(0)).isEmpty()) {
                                    line[7] = NewMainFrame.getStringValue(r.getCell(0));
                                    line[8] = NewMainFrame.getStringValue(r.getCell(1));
                                    line[9] = NewMainFrame.getStringValue(r.getCell(2));
                                    r = sh.getRow(r.getRowNum() + 1);
                                    writer.writeNext(line);
                                    
                                    Arrays.fill(line, "");
                                }
                            } catch (Exception e) {
                                e.printStackTrace();
                                break;
                            }

                        } catch (IllegalStateException ex) {
                            ex.printStackTrace();
                        }

                    }

                    

                } catch (Exception ex) {
                    Logger.getLogger(App.class.getName()).log(Level.SEVERE, "could not load " + xlsFile.getName(), ex);
                }


            }
        }
        writer.close();
        setVisible(false);
    }
